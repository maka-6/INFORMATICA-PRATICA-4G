package Primo_lavoro;

public class Punto {
    private float x;
    private float y;
    // commento
    Punto(float x, float y) {
        this.x = x;
        this.y = y;
    }
    // costruttore
    // getter
    float getX() {
        return this.x;
    }
    float getY() {
        return this.y;
    }
}
